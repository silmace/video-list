const localeOptions = [
  { title: '中文', value: 'zh-CN' as AppLocale },
  { title: 'English', value: 'en-US' as AppLocale },
];

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

function setLocale(next: AppLocale): void {
  locale.value = next;
  localStorage.setItem(LOCALE_KEY, next);
}

function t(key: string, params?: Record<string, string | number>): string {
  const table = messages[locale.value] || messages['en-US'];
  const fallback = messages['en-US'];
  const value = table[key] || fallback[key] || key;
  return interpolate(value, params);
}

export function useLocale() {
  return {
    locale,
    localeOptions,
    setLocale,
    t,
    isZh: computed(() => locale.value === 'zh-CN'),
  };
}
